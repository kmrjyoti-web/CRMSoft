// ─── Seed Function ───────────────────────────────────────────────────────────

export async function seedControlRoomRules(prisma: PrismaService): Promise<void> {
  const results = await Promise.all(
    CONTROL_ROOM_RULES.map((rule) =>
      prisma.controlRoomRule.upsert({
        where: { ruleCode: rule.ruleCode },
        update: {
          category: rule.category,
          subCategory: rule.subCategory ?? null,
          label: rule.label,
          description: rule.description ?? null,
          valueType: rule.valueType,
          defaultValue: rule.defaultValue,
          selectOptions: (rule.selectOptions as any) ?? undefined,
          minValue: rule.minValue ?? null,
          maxValue: rule.maxValue ?? null,
          sortOrder: rule.sortOrder,
          allowedLevels: JSON.parse(rule.allowedLevels),
          industrySpecific: rule.industrySpecific,
        },
        create: {
          ruleCode: rule.ruleCode,
          category: rule.category,
          subCategory: rule.subCategory ?? null,
          label: rule.label,
          description: rule.description ?? null,
          valueType: rule.valueType,
          defaultValue: rule.defaultValue,
          selectOptions: (rule.selectOptions as any) ?? undefined,
          minValue: rule.minValue ?? null,
          maxValue: rule.maxValue ?? null,
          sortOrder: rule.sortOrder,
          allowedLevels: JSON.parse(rule.allowedLevels),
          industrySpecific: rule.industrySpecific,
        },
      }),
    ),
  );

  console.log(`[ControlRoom] Seeded ${results.length} rules across categories.`);
}
