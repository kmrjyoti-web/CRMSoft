## Golden Rules (Quick Reference)

1. **Discovery-first** — check exist → update; not exist → create. Zero duplicacy
2. **Backend + Frontend together** — every feature has API + UI in one prompt
3. **CQRS+DDD pattern** — commands/, queries/, handlers/, domain/, presentation/
4. **AIC shared controls only** — AICTable, AICDrawer, AICToolbar. No exceptions
5. **API UX pattern** — loading skeleton → disable button on submit → toast on error/success
6. **Tenant isolation** — all queries filter by tenantId
7. **Production-safe** — backup before migration, rollback plan required
8. **Version-based changes** — v1 → v2 → v3, never delete old directly
9. **Module → Menu first** — new module needs menu entry before it works
10. **Audit everything** — every action logged

