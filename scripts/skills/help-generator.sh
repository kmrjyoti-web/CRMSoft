## Troubleshooting

### API won't start
```bash
pnpm kill                     # kill port conflicts
cd Application/backend && pnpm typecheck   # check TS errors
```

### DB connection failed
```bash
cat Application/backend/.env | grep DATABASE_URL   # verify URLs
railway status                # check Railway services
```

### Audit:db failing
```bash
cd Application/backend && pnpm audit:db    # run directly for full output
# Check: 19 module codes, vertical prefixes, cross-DB includes
```

### Prisma client not generated
```bash
cd Application/backend && pnpm prisma:generate:all
```

### Husky hooks failing
```bash
echo "feat(test): message" | npx commitlint   # test commitlint
bash scripts/architecture-guard.sh            # test guard
```

